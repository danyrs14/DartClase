void main() {
  //Sentencias de control y flujo
  int dia = 7;
  if (dia == 1) {
    print('El dia es Lunes');
  } else if (dia == 2) {
    print('El dia es martes');
  } else if (dia == 3) {
    print('El dia es miercoles');
  } else if (dia == 4) {
    print('El dia es jueves');
  } else if (dia == 5) {
    print('El dia es igual a viernes');
  } else {
    print('Es fin de semana');
  }
}
